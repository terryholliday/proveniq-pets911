export function getCountiesByComplianceTier(tier: CountyComplianceData['compliance_tier']): County[] {
  return Object.entries(COUNTY_COMPLIANCE_DATA)
    .filter(([_, data]) => data.compliance_tier === tier)
    .map(([county]) => county as County);
}

